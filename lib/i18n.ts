export function getLanguageInstruction(lang: string): string {
  if (lang === "en") return "All text values in the JSON (summary, finding, detail, recommendation_text, disclaimer) MUST be in English.";
  const name = LANG_NAMES[lang] || lang;
  return `IMPORTANT: All text values in the JSON output (summary, finding, detail, recommendation_text, disclaimer) MUST be written in ${name} (${lang.toUpperCase()}). The JSON keys stay in English, but ALL text content must be in ${name}. This is critical — do NOT respond in English.`;
}
